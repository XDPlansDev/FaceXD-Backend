/**
 * @route   PUT /api/users/me
 * @desc    Atualizar perfil do usu√°rio autenticado
 * @access  Privado
 */
router.put("/me", authenticateToken, async (req, res) => {
  try {
    console.log("üìù Atualizando perfil do usu√°rio:", req.user.id);
    console.log("Dados recebidos:", req.body);

    const user = await User.findById(req.user.id);
    if (!user) {
      console.warn("‚ö†Ô∏è Usu√°rio n√£o encontrado para atualiza√ß√£o");
      return res.status(404).json({ message: "Usu√°rio n√£o encontrado" });
    }

    // Campos permitidos para atualiza√ß√£o
    const allowedUpdates = ['nome', 'sobrenome', 'bio', 'sexo', 'dataNascimento', 'username'];
    const updates = Object.keys(req.body)
      .filter(key => allowedUpdates.includes(key))
      .reduce((obj, key) => {
        obj[key] = req.body[key];
        return obj;
      }, {});

    // Se tentar atualizar o username, verifica se j√° passou o tempo m√≠nimo
    if (updates.username && updates.username !== user.username) {
      if (user.usernameChangedAt) {
        const lastChange = new Date(user.usernameChangedAt);
        const now = new Date();
        const diffDays = Math.floor((now - lastChange) / (1000 * 60 * 60 * 24));

        if (diffDays < 30) {
          return res.status(400).json({
            message: "Voc√™ s√≥ pode alterar seu username a cada 30 dias"
          });
        }
      }

      // Atualiza a data da √∫ltima mudan√ßa de username
      updates.usernameChangedAt = new Date();
    }

    // Atualiza os campos
    Object.keys(updates).forEach(key => {
      user[key] = updates[key];
    });

    await user.save();
    console.log("‚úÖ Perfil atualizado com sucesso");

    // Retorna o usu√°rio atualizado sem a senha
    const updatedUser = user.toObject();
    delete updatedUser.password;

    res.json(updatedUser);
  } catch (err) {
    console.error("‚ùå Erro ao atualizar perfil:", err);
    res.status(500).json({ message: "Erro ao atualizar perfil" });
  }
});

